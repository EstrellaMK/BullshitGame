    public void buscarGanador(String[] playerIds, ArrayList<ArrayList<Card>> playerHand, String winner){
       int a[] = new int[4];
       int contador = 0;
        
       
        for (int i = 0; i < playerIds.length; i++) {
            if (playerIds[i] != winner) {
                a[contador] = i;
                contador++;
            }
        }
        
       winnerLabel.setText(winner);
        
        if(playerIds.length<3){
            p2.setText(playerIds[a[0]]);
            p2cards.setText(String.valueOf(playerHand.get(a[0]).size()));
            p2Text.setText("cartas");
        }else if(playerIds.length<4){
            
            p2.setText(playerIds[a[0]]);
            p2cards.setText(String.valueOf(playerHand.get(a[0]).size()));
            p2Text.setText("cartas");
            
            p3.setText(playerIds[a[1]]);
            p3cards.setText(String.valueOf(playerHand.get(a[1]).size()));
            p3Text.setText("cartas");
        
        }else{
            p2.setText(playerIds[a[0]]);
            p2cards.setText(String.valueOf(playerHand.get(a[0]).size()));
            p2Text.setText("cartas");
            
            p3.setText(playerIds[a[1]]);
            p3cards.setText(String.valueOf(playerHand.get(a[1]).size()));
            p3Text.setText("cartas");
            
            p4.setText(playerIds[a[2]]);
            p4cards.setText(String.valueOf(playerHand.get(a[2]).size()));
            p4Text.setText("cartas");
        }
           
    }

